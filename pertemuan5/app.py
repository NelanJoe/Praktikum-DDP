# Buatlah fungsi yang bisa membuat perkalian
# Buatlah fungsi yang bisa membuat pembagian

def perkalian(bilanganPertama, bilanganKedua):
    total = bilanganPertama * bilanganKedua
    return total

def pembagian(bilanganPertama, bilanganKedua):
    total = int(bilanganPertama / bilanganKedua)
    return total



print(perkalian(10, 5))
print(pembagian(10, 2))